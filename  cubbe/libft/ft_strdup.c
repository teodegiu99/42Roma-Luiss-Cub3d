#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*ptr;

	i = 0;
	while (s1[i])
		i++;
	ptr = (char *)malloc(i + 1);
	if (!ptr)
		return (0);
	ft_strlcpy(ptr, s1, (size_t)i + 1);
	return (ptr);
}
